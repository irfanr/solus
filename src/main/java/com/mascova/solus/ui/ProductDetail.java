   private void setKeyBindings() {
        closeAction = new AbstractAction("closeAction") {
            @Override
            public void actionPerformed(ActionEvent e) {
                try {
                    closeFrame();
                } catch (PropertyVetoException ex) {
                    Logger.getLogger(ProductMaster.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        };
        closeAction.putValue(
                Action.ACCELERATOR_KEY,
                KeyStroke.getKeyStroke(KeyEvent.VK_W, Event.CTRL_MASK));

        this.getActionMap().put("closeAction", closeAction);
        this.getInputMap(JComponent.WHEN_IN_FOCUSED_WINDOW).put(
                (KeyStroke) closeAction.getValue(
                Action.ACCELERATOR_KEY), "closeAction");
    }    

